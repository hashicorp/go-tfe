func generateHelpers(schemas []*SchemaInfo, path string) error {
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, schemas); err != nil {
		return fmt.Errorf("executing template: %w", err)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		// Write unformatted output so the caller can debug template issues.
		_ = os.WriteFile(path, buf.Bytes(), 0o644)
		return fmt.Errorf("formatting generated code (unformatted output written to %s): %w", path, err)
	}

	return os.WriteFile(path, src, 0o644)
}
